    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Materia materia = new Materia();
        try {
            if (jTextField1.getText().trim().isEmpty() ||
                   jTextField4.getText().trim().isEmpty() ) {
                JOptionPane.showMessageDialog(this, "Debe llenar todos los espacios");
            } else {
                materia.setId(jTextField4.getText()+e.getUsuario());
                materia.setNombre(jTextField1.getText());
                materia.setNota(Double.parseDouble(jTextField2.getText()));
                materia.setCreditos(Integer.parseInt(jTextField3.getText()));
                if (materia.getCreditos() <= 0 || materia.getNota() < 0) {
                    JOptionPane.showMessageDialog(this, "Ingrese un valor valido");
                } else {

                    jTextField1.setText("");
                    jTextField2.setText("");
                    jTextField3.setText("");
                    jTextField4.setText("");
                    boolean repetido = false;
                    for (int i = 0; i < e.getCarrera().getMaterias().size(); i++) {
                        Materia a = e.getCarrera().getMaterias().get(i);
                        if (a.getId().equals(materia.getId())) {
                            repetido = true;
                        }
                    }
                    if (!repetido) {
                        materiadb.insertarMateria(e, materia);
                        e.getCarrera().getMaterias().add(materia);
                        JOptionPane.showMessageDialog(this, "Materia agregada");
                    } else {
                        JOptionPane.showMessageDialog(this, "ID repetido");
                    }
                }
            }
        } catch (NumberFormatException n) {
            JOptionPane.showMessageDialog(this, "No ingreso un nÃºmero");
        } 

    }//GEN-LAST:event_jButton1ActionPerformed

