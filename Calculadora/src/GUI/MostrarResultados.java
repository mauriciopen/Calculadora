    public void recibirDatos(Estudiante estudiante){
        e = estudiante;
        e.getCarrera().obtenerCursados();
        e.getCarrera().calcularAvance();
        e.getCarrera().ponderadoAcumulado();
        
    }
    
    public boolean verificarCreditos(){
        if (e.getCarrera().getAvance() > 100) {
            JOptionPane.showMessageDialog(this, "La suma de los creditos es"
                    + " mayor que la cantidad de creditos m√°xima.\n"
                    + "Intente de nuevo");
            e.getCarrera().getMaterias().clear();
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            materias = new IngresarMaterias();
            e.getCarrera().getMaterias().clear();
            materias.recibirDatos(e);
            materias.setVisible(true);
            return false;
        }else{
            return true;
        }
    }
    public void setResultados() {
        jTextField1.setText(e.getNombre());
        jTextField2.setText(e.getNombreCarrera());
        jProgressBar1.setValue((int) e.getCarrera().getAvance());
        double porcentaje = e.getCarrera().getPonderado();
        String por = String.format("%.3f", porcentaje);
        jTextField3.setText(por);
        for (int i = 0; i < e.getCarrera().getMaterias().size(); i++) {
            Materia actual = e.getCarrera().getMaterias().get(i);
            model.addRow(new Object[]{actual.getId(),actual.getNombre(), actual.getNota(), actual.getCreditos()});
        }
        estudiantedb.actualizarEstudiante(e);
    }

